/**
函数意义:将一段经常使用的代码封装起来，减少重复代码.一个较大的程序，一般分为若干个程序块，每个模块实现特定的功能。
函数的定义一般主要有5个步骤：
      1、返回值类型  int xxxx(xx, xx)
      2、函数名      xxx name(xx, xx)
      3、参数表列    xxx xxxx(int a, char b)
      4、函数体语句  xxx xxxx(xx, xx)
                     {
                        函数实现的功能;

                        xxxxx;
                     }

5、return 表达式     xxx xxxx(xx, xx)
                     {
                        xxxxxxxxxxxx;

                        return value;
                     }
**/
